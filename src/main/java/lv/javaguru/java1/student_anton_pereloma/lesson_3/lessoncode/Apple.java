package lv.javaguru.java1.student_anton_pereloma.lesson_3.lessoncode;

class Apple {
    private String type;
    private String color;
    private int weight;

    public Apple(String type, String color, int weight) {
        this.type = type;
        this.weight = weight;
        this.color = color;
    }

    public String getType() {
        return this.type;
    }
    public String getColor() {
        return this.color;
    }
    public int getWeight() {
        return this.weight;
    }



}

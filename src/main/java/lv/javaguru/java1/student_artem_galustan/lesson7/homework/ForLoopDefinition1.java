public class ForLoopDefinition1 {
    public static void main(String[] args) {
        for (int i = 0; i < 10; i++) {
            System.out.println(i);
        }
    }


}
//В данном коде не обозначена переменная i для сравнения с 10ткой для начала цыкла в котором результат доходит до 10 в консоле 9 ибо с 0 начало.
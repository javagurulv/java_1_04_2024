class DefaultConstructorV3 {

    private String fullName;
    private int age;
    private boolean male;
    private Boolean female;

    String getFullName(){
        return this.fullName;
    }

    int getAge(){
        return this.age;
    }

    boolean getMale(){
        return this.male;
    }

    Boolean getFemale(){
        return this.female;
    }

}

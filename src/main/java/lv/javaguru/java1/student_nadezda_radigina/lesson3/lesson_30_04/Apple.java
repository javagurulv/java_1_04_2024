package lv.javaguru.java1.student_nadezda_radigina.lesson3.lesson_30_04;

 class Apple {
     private String type;
     private String color;
     private int weight;


     public Apple(String type, String color, int weight) {
         this.type = type;
         this.color = color;
         this.weight = weight;
     }

     public String getType() {
         return type;
     }

     public String getColor() {
         return color;
     }

     public int getWeight() {
         return weight;
     }
 }

package lv.javaguru.java1.student_pavels_zuriha.lesson_2.lessoncode;

public class CalculatorDouble {

    public static void main(String[] args) {

        double number1 = 100;
        double number2 = 200;

        double additionResult = number1 + number2;
        double subtractResult = number1 - number2;
        double multiplicationResult = number1 * number2;
        double divisionResult = number1 / number2;

        System.out.println("Result of Addition = " + additionResult);
        System.out.println("Result of Subtraction = " + subtractResult);
        System.out.println("Result of Multiple = " + multiplicationResult);
        System.out.println("Result of Division = " + divisionResult);

    }
}
